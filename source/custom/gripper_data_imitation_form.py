"""
demo_data_imitation_form.py

Convert multimodal merged demonstration HDF5 into per-episode imitation-learning
HDF5 files for nrs_imitation.

This converter matches the recorder layout generated by:

  vr_demo_hdf5_recorder.py  (multimodal version)

Expected merged HDF5 layout:

  merged.hdf5
  └── episodes/
      ├── ep_0000/
      │   ├── position                  (T, 6)
      │   ├── ft                        (T, 3)
      │   ├── images/
      │   │   ├── cam0                  (T, H, W, 3)
      │   │   └── cam1                  (T, H, W, 3) optional/global cam
      │   ├── marker/
      │   │   ├── id0                   (T, 7) [x,y,z,rx,ry,rz,valid]
      │   │   ├── id1                   (T, 7) [x,y,z,rx,ry,rz,valid]
      │   │   ├── combined              (T,14)
      │   │   ├── id0_quat              (T, 8) optional
      │   │   └── id1_quat              (T, 8) optional
      │   ├── gripper/
      │   │   ├── present_position      (T,)
      │   │   └── present_current_mA    (T,)
      │   └── marker                    (T,14) optional top-level alias
      └── ep_0001/
          └── ...

Output per-episode HDF5 layout:

  episode_0.hdf5
  ├── action/
  │   ├── position                      (T_out, 6)
  │   ├── force                         (T_out, 3)
  │   └── gripper_present_position      (T_out,)
  ├── observations/
  │   ├── position                      (T_out, 6)
  │   ├── force                         (T_out, 3)
  │   ├── marker                        (T_out,14) [id0(7), id1(7)]
  │   ├── marker_id0                    (T_out, 7)
  │   ├── marker_id1                    (T_out, 7)
  │   ├── marker_id0_quat               (T_out, 8), if available
  │   ├── marker_id1_quat               (T_out, 8), if available
  │   ├── gripper/
  │   │   ├── present_position          (T_out,)
  │   │   └── present_current_mA        (T_out,)
  │   ├── images/
  │   │   ├── cam0                      (T_out,H,W,3)
  │   │   └── cam1                      (T_out,H,W,3)
  │   └── is_pad                        (T_out,)
  └── meta/
      attrs:
        orig_len, T_pad, pad_starts_at, truncated, camera_names_json, marker_dim

Core observation for training:

  qpos   = position(6) + force(3)
  image  = cam0 or cam0+cam1 depending on obs_mode
  marker = marker id0 + id1 = (T,14)

Marker convention:
  id0 = robot EE marker or VR tracker top marker
  id1 = workpiece/surface marker
"""

