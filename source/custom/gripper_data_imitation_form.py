def make_zero_marker(T: int, dim: int = 7) -> np.ndarray:
    return np.zeros((int(T), int(dim)), dtype=np.float32)


def normalize_marker7(marker: Optional[np.ndarray], T: int, name: str) -> np.ndarray:
    """
    Return (T,7) marker.
    Accepted:
      - None -> zeros
      - (T,7) [x,y,z,rx,ry,rz,valid]
      - (T,6) [x,y,z,rx,ry,rz], valid=1
      - (T,8) [x,y,z,qx,qy,qz,qw,valid] is NOT converted here.
    """
    if marker is None:
        return make_zero_marker(T, 7)

    marker = np.asarray(marker, dtype=np.float32)
    if marker.ndim != 2:
        raise ValueError(f"{name} marker must be 2D, got {marker.shape}")

    marker = marker[:T]

    if marker.shape[1] == 7:
        return marker.astype(np.float32)

    if marker.shape[1] == 6:
        valid = np.ones((marker.shape[0], 1), dtype=np.float32)
        return np.concatenate([marker, valid], axis=1).astype(np.float32)

    raise ValueError(
        f"{name} marker must have dim 6 or 7. "
        f"Expected rotvec marker [x,y,z,rx,ry,rz,(valid)], got {marker.shape}"
    )


def normalize_marker8_quat(marker: Optional[np.ndarray], T: int) -> Optional[np.ndarray]:
    """
    Return optional (T,8) quat marker.
    Accepted:
      - None -> None
      - (T,8) [x,y,z,qx,qy,qz,qw,valid]
      - (T,7) [x,y,z,qx,qy,qz,qw], valid=1
    """
    if marker is None:
        return None
    marker = np.asarray(marker, dtype=np.float32)
    if marker.ndim != 2:
        return None
    marker = marker[:T]

    if marker.shape[1] == 8:
        return marker.astype(np.float32)
    if marker.shape[1] == 7:
        valid = np.ones((marker.shape[0], 1), dtype=np.float32)
        return np.concatenate([marker, valid], axis=1).astype(np.float32)
    return None


def load_marker_pair(g_ep: h5py.Group, T: int, require_marker: bool) -> Tuple[np.ndarray, np.ndarray, np.ndarray, Optional[np.ndarray], Optional[np.ndarray], Dict[str, str]]:
    """
    Load id0/id1 marker and combined marker from an input episode group.
    """
    source_paths: Dict[str, str] = {}

    # Preferred recorder paths.
    id0, p0 = _first_existing_array(
        g_ep,
        [
            "marker/id0",
            "observations/marker_id0",
            "observations/marker/id0",
            "aruco/id0",
            "aruco/id_0",
        ],
        dtype=np.float32,
    )
    id1, p1 = _first_existing_array(
        g_ep,
        [
            "marker/id1",
            "observations/marker_id1",
            "observations/marker/id1",
            "aruco/id1",
            "aruco/id_1",
        ],
        dtype=np.float32,
    )

    combined, pc = _first_existing_array(
        g_ep,
        [
            "marker/combined",
            "marker",
            "observations/marker",
            "aruco/combined",
        ],
        dtype=np.float32,
    )

    if combined is not None and combined.ndim == 2 and combined.shape[1] >= 14:
        combined = combined[:T, :14].astype(np.float32)
        if id0 is None:
            id0 = combined[:, :7]
            p0 = pc + "[:, :7]"
        if id1 is None:
            id1 = combined[:, 7:14]
            p1 = pc + "[:, 7:14]"

    if require_marker and (id0 is None or id1 is None):
        raise KeyError(
            f"require_marker=True but marker id0/id1 not found in {g_ep.name}. "
            f"Found paths: id0={p0}, id1={p1}, combined={pc}"
        )

    id0 = normalize_marker7(id0, T, "id0")
    id1 = normalize_marker7(id1, T, "id1")
    combined = np.concatenate([id0, id1], axis=1).astype(np.float32)

    id0q, p0q = _first_existing_array(
        g_ep,
        [
            "marker/id0_quat",
            "observations/marker_id0_quat",
            "aruco/id0_quat",
        ],
        dtype=np.float32,
    )
    id1q, p1q = _first_existing_array(
        g_ep,
        [
            "marker/id1_quat",
            "observations/marker_id1_quat",
            "aruco/id1_quat",
        ],
        dtype=np.float32,
    )

    id0q = normalize_marker8_quat(id0q, T)
    id1q = normalize_marker8_quat(id1q, T)

    source_paths["marker_id0"] = p0
    source_paths["marker_id1"] = p1
    source_paths["marker_combined"] = pc
    source_paths["marker_id0_quat"] = p0q
    source_paths["marker_id1_quat"] = p1q

    return id0, id1, combined, id0q, id1q, source_paths


