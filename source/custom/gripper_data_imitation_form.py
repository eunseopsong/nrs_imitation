def parse_args():
    parser = argparse.ArgumentParser(
        description="Convert multimodal merged HDF5 to per-episode imitation HDF5."
    )

    parser.add_argument(
        "--input_h5",
        type=str,
        default="",
        help="Path to merged HDF5. If empty, use latest under --act_root.",
    )
    parser.add_argument(
        "--act_root",
        type=str,
        default=ACT_ROOT_DEFAULT,
        help="Root used for auto-latest merged HDF5 search.",
    )
    parser.add_argument(
        "--output_dir",
        type=str,
        default="",
        help="Output directory. If empty, inferred as <dataset_time>/episodes_multimodal.",
    )
    parser.add_argument(
        "--output_name",
        type=str,
        default="episodes_multimodal",
        help="Output folder name when --output_dir is empty.",
    )

    parser.add_argument("--require_cam1", action="store_true", help="Fail episode if cam1/global image is missing.")
    parser.add_argument("--require_marker", action="store_true", help="Fail episode if marker id0/id1 is missing.")

    parser.add_argument("--min_len", type=int, default=10)
    parser.add_argument("--max_len", type=int, default=0, help="0 means no truncation.")

    parser.add_argument("--compression", type=str, default="gzip", choices=["gzip", "lzf", "none"])
    parser.add_argument("--gzip_level", type=int, default=4)

    parser.add_argument("--overwrite", action="store_true", help="Overwrite output dir if it exists.")
    parser.add_argument("--keep_failed", action="store_true", help="Continue conversion and keep summary of failures.")

    return parser.parse_args()


def main():
    args = parse_args()

    input_h5 = args.input_h5.strip()
    if not input_h5:
        input_h5 = find_latest_merged_h5(args.act_root)

    output_dir = args.output_dir.strip()
    if not output_dir:
        output_dir = infer_output_dir_from_input(input_h5, args.output_name)

    compression = str(args.compression).lower()
    if compression == "none":
        compression = "none"

    convert_merged_h5(
        input_h5=input_h5,
        output_dir=output_dir,
        require_cam1=bool(args.require_cam1),
        require_marker=bool(args.require_marker),
        min_len=int(args.min_len),
        max_len=int(args.max_len),
        compression=compression,
        gzip_level=int(args.gzip_level),
        overwrite=bool(args.overwrite),
        keep_failed=bool(args.keep_failed),
    )


if __name__ == "__main__":
    main()
