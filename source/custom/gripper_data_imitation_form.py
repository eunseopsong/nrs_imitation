def find_latest_merged_h5(act_root: str) -> str:
    patterns = [
        os.path.join(act_root, "*", "merged_hdf5", "*.hdf5"),
        os.path.join(act_root, "*", "merged_hdf5", "*.h5"),
    ]
    files: List[str] = []
    for p in patterns:
        files.extend(glob.glob(p))
    files = sorted(set(files))
    if not files:
        raise FileNotFoundError(
            f"No merged HDF5 found under {act_root}/*/merged_hdf5/*.hdf5"
        )
    return files[-1]


def infer_output_dir_from_input(input_h5: str, output_name: str) -> str:
    p = Path(input_h5).resolve()
    # .../datasets/ACT/YYYYMMDD_HHMM/merged_hdf5/file.hdf5
    if p.parent.name == "merged_hdf5":
        return str(p.parent.parent / output_name)
    return str(p.parent / output_name)


