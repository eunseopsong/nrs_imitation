def _has_path(g: h5py.Group, path: str) -> bool:
    try:
        _ = g[path]
        return True
    except Exception:
        return False


def _read_array(g: h5py.Group, path: str, dtype=None):
    if not _has_path(g, path):
        raise KeyError(f"Missing required dataset: {g.name}/{path}")
    arr = np.asarray(g[path])
    if dtype is not None:
        arr = arr.astype(dtype)
    return arr


def _read_optional_array(g: h5py.Group, path: str, dtype=None):
    if not _has_path(g, path):
        return None
    arr = np.asarray(g[path])
    if dtype is not None:
        arr = arr.astype(dtype)
    return arr


def _first_existing_array(g: h5py.Group, paths: List[str], dtype=None):
    for p in paths:
        arr = _read_optional_array(g, p, dtype=dtype)
        if arr is not None:
            return arr, p
    return None, ""


def _ensure_2d(arr: np.ndarray, dim: int, name: str) -> np.ndarray:
    arr = np.asarray(arr)
    if arr.ndim == 1 and arr.size == dim:
        arr = arr.reshape(1, dim)
    if arr.ndim != 2 or arr.shape[1] != dim:
        raise ValueError(f"{name} must be (T,{dim}), got {arr.shape}")
    return arr


def _ensure_image4(arr: np.ndarray, name: str) -> np.ndarray:
    arr = np.asarray(arr)
    if arr.ndim != 4 or arr.shape[-1] != 3:
        raise ValueError(f"{name} must be (T,H,W,3), got {arr.shape}")
    if arr.dtype != np.uint8:
        # Clip instead of failing. Some recorders may save float RGB.
        arr = np.clip(arr, 0, 255).astype(np.uint8)
    return arr


def _ensure_vector1(arr: np.ndarray, name: str) -> np.ndarray:
    arr = np.asarray(arr)
    if arr.ndim == 0:
        arr = arr.reshape(1)
    if arr.ndim == 2 and arr.shape[1] == 1:
        arr = arr[:, 0]
    if arr.ndim != 1:
        raise ValueError(f"{name} must be (T,) or (T,1), got {arr.shape}")
    return arr


def _trim_to_min_len(items: Dict[str, np.ndarray]) -> Tuple[Dict[str, np.ndarray], int]:
    lengths = []
    for k, v in items.items():
        if v is not None:
            lengths.append(int(v.shape[0]))
    if not lengths:
        raise ValueError("No arrays to trim.")
    T = min(lengths)
    out = {}
    for k, v in items.items():
        if v is None:
            out[k] = None
        else:
            out[k] = v[:T]
    return out, T


